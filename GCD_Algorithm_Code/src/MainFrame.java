    public BigInteger DigitNumBig() {
        int x = Integer.valueOf(BigNumTextField.getText());

        BigInteger max = (BigInteger) (BigInteger.TEN.pow(x)).subtract(BigInteger.ONE);
        BigInteger min = (BigInteger) (BigInteger.TEN.pow(x - 1));
        BigInteger delta = max.subtract(min);
        Random rnd = new Random();
        BigInteger n1 = new BigInteger(delta.bitLength(), rnd);

        do {
            n1 = new BigInteger(delta.bitLength(), rnd);
        } while (n1.compareTo(delta) > 0);

        return n1.add(min);

    }

    public ArrayList<Result> loadFromCSV(String fileName) throws IOException {
        try ( BufferedReader in = new BufferedReader(new FileReader(fileName))) {
            String l = in.readLine();
            ArrayList<Result> alResults = new ArrayList<>();
            while ((l = in.readLine()) != null) {
                String[] ar = l.split(";");
                alResults.add(new Result(BigInteger.valueOf(Long.valueOf(ar[2])),BigInteger.valueOf(Long.valueOf(ar[1])),BigInteger.valueOf(Long.valueOf(ar[3])),Double.valueOf(ar[4]),ar[0]));
            }
            return alResults;
        }
    }
    
    public void saveToCSV(String fileName, ArrayList<Result> alResults) throws IOException {
        try ( PrintWriter out = new PrintWriter(new FileWriter(fileName))) {
            out.println("Algorithms;First_number;Second_number;gcd;Time");
            for (Result x : alResults) {
                out.println(x.toCSV());
            }
        }
    }

