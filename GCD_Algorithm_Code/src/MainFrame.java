    private void loadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtonActionPerformed
        JFileChooser jFileChooser = new JFileChooser(currentFile);
        jFileChooser.setAcceptAllFileFilterUsed(false);
        jFileChooser.addChoosableFileFilter(new FileNameExtensionFilter(".CSV Files", "csv"));
        int returnVal = jFileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            currentFile = jFileChooser.getSelectedFile();
            try {
                alBigIn = loadFromCSV(jFileChooser.getSelectedFile().getAbsolutePath());
                ImageIcon iconic=new ImageIcon("EsadaL.png");
                JOptionPane.showMessageDialog(null, "File has been sucessfully loaded","Message", JOptionPane.INFORMATION_MESSAGE,iconic);
            } catch (Exception e) {
                System.err.println(e);
            }
        }
        viewList.setListData(BigInToArray());
    }//GEN-LAST:event_loadButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        JFileChooser jFileChooser = new JFileChooser(currentFile);
        jFileChooser.setFileFilter(new FileNameExtensionFilter(".CSV Files", "csv"));
        int returnVal = jFileChooser.showSaveDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            currentFile = jFileChooser.getSelectedFile();
            try {
                String absolutPath = jFileChooser.getSelectedFile().getAbsolutePath();
                if (!absolutPath.substring(absolutPath.length() - 4).equals(".csv")) {
                    absolutPath += ".csv";
                    currentFile = new File(absolutPath);
                }
                saveToCSV(absolutPath, alBigIn);
                ImageIcon iconic=new ImageIcon("EsadaL.png");
                JOptionPane.showMessageDialog(null, "File has been sucessfully saved","Message", JOptionPane.INFORMATION_MESSAGE,iconic);
            } catch (Exception e) {
                System.err.println(e);
            }
        }

    }//GEN-LAST:event_saveButtonActionPerformed

